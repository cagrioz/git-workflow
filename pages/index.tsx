import Head from "next/head";
import Commit from "../components/Exercises/Commit";
import Footer from "../components/Footer";
import Header from "../components/Header";
import SnackbarProvider from "react-simple-snackbar";
import Branch from "../components/Exercises/Branch";
import Merge from "../components/Exercises/Merge";
import { useEffect, useState } from "react";
import CommitMerge from "../components/Exercises/CommitMerge";

export default function Home() {
    const [exercises, setExercises] = useState<any[]>([]);

    // Fetch exercises from API
    useEffect(() => {
        fetch("http://localhost:8000/exercises")
            .then((res) => res.json())
            .then((data) => setExercises(data));
    }, []);

    return (
        <div>
            <Head>
                <title>GitHub Workflow Teacher</title>
                <meta name="description" content="Generated by create next app" />
                <link rel="icon" href="/favicon.ico" />
            </Head>
            <div className="min-h-screen">
                <Header />

                <SnackbarProvider>
                    <div className="container mx-auto">
                        <div className="flex flex-col gap-10">
                            {exercises.length > 0 &&
                                exercises.map((exercise) => {
                                    return (
                                        <div className="flex flex-col gap-5" key={exercise._exerciseName}>
                                            <div className="flex flex-col gap-2">
                                                <h2 className="text-4xl font-bold">{exercise._exerciseName}</h2>
                                                <p className="whitespace-pre-line mt-4">{exercise._description}</p>
                                            </div>
                                            {exercise._exerciseId === 1 && <Commit />}
                                            {exercise._exerciseId === 2 && <Branch />}
                                            {exercise._exerciseId === 3 && <Merge />}
                                        </div>
                                    );
                                })}
                            <div className="flex flex-col gap-5">
                                <div className="flex flex-col gap-2">
                                    <h2 className="text-4xl font-bold">Test</h2>
                                    <p className="whitespace-pre-line mt-4">
                                        Lorem ipsum, dolor sit amet consectetur adipisicing elit. Optio molestias
                                        asperiores animi sequi velit maxime itaque at nulla tempore id.
                                    </p>
                                </div>
                                <CommitMerge />
                            </div>
                        </div>
                    </div>
                </SnackbarProvider>

                <Footer />
            </div>
        </div>
    );
}
