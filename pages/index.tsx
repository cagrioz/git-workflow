import { Gitgraph, Orientation, TemplateName } from "@gitgraph/react";
import Head from "next/head";
import { useState } from "react";
import Footer from "../components/Footer";
import Header from "../components/Header";

export default function Home() {
    const [commits, setCommits] = useState<string[]>(["Initial commit"]);
    const [commit, setCommit] = useState<string>("");

    function addCommit() {
        setCommits([...commits, commit]);
        setCommit("");
    }

    return (
        <div>
            <Head>
                <title>GitHub Workflow Teacher</title>
                <meta name="description" content="Generated by create next app" />
                <link rel="icon" href="/favicon.ico" />
            </Head>
            <Header />
            <div className="mx-auto text-center">
                <input
                    type="text"
                    className="border-2 border-gray-300 bg-white h-10 px-5 pr-16 rounded-lg text-sm focus:outline-none"
                    placeholder="Commit Message"
                    onChange={(e) => setCommit(e.target.value)}
                    value={commit}
                />
                <button
                    className="bg-blue-500 hover:bg-blue-700 text-white font-bold py-2 px-4 rounded"
                    onClick={addCommit}
                >
                    Add Commit
                </button>
            </div>
            <Gitgraph options={{}} key={commits.length}>
                {(gitgraph) => {
                    // Simulate git commands with Gitgraph API.
                    const master = gitgraph.branch("master");

                    commits.forEach((commit) => {
                        master.commit(commit);
                    });
                }}
            </Gitgraph>
            <Footer />
        </div>
    );
}
